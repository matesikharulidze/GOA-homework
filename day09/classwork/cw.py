# 1. მონაცემების შეყვანა input() ფუნქციით
text = input("შეიყვანეთ ტექსტი: ")  # სტრიქონი (str)
number = int(input("შეიყვანეთ მთელი რიცხვი: "))  # მთელი რიცხვი (int)
decimal = float(input("შეიყვანეთ ათობითი რიცხვი: "))  # ათობითი რიცხვი (float)

# 2. მონაცემთა ტიპების შემოწმება
print("\nმონაცემთა ტიპები:")
print(f"text ცვლადის ტიპი: {type(text)}")
print(f"number ცვლადის ტიპი: {type(number)}")
print(f"decimal ცვლადის ტიპი: {type(decimal)}")

# 3. კონკატინაციის სამი მაგალითი

# (1) ტექსტის გაერთიანება
concat1 = text + " არის საუკეთესო!"
print("\nკონკატინაცია 1:", concat1)

# (2) რიცხვისა და ტექსტის გაერთიანება (str() გამოყენებით)
concat2 = text + " - შენი შეყვანილი რიცხვია: " + str(number)
print("კონკატინაცია 2:", concat2)

# (3) f-სტრიქონის გამოყენება
concat3 = f"{text} - შენი შეყვანილი რიცხვები: {number} და {decimal}"
print("კონკატინაცია 3:", concat3)
